/* 
    ------------ ARRAY ----------
    => IT IS USED TO STORE MULTIPLE VALUES IN A SINGLE VARIABLE.
    => THESE VALUE COULD BE DIFFERENT AT THE SAME TIME.
    => THERE IS NO NEED TO STORE ONLY NUMBER OR ONLY STRING AT THE SAME TIME.

    EX: LET B = ['A','B','C']; // ARRAY OF STRINGS

    EX: LET B = [1,2,3,4]; // ARRAY OF NUMBERS

    EX: LET A = [1, 'HELLO', TRUE] ; // ARRAY WITH  DIFFERENT TYPES OF VALUES

*/

