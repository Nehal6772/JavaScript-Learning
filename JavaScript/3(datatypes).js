
/*
    THERE ARE 6 TYPES OF DATA TYPE IN JAVASCRIPT.
    1) NUMBER
    2) STRING
    3) BOOLEAN
    4) ARRAY
    5) OBJECT
    6) UNDEFINED
*/


/* 
    1) WE CAN CHANGE THE DATATYPE IN JAVASCRIPT (DYNAMIC VARIABLE)

    EX: LET A = 5; //NUMBER
        A = "NEHAL"; // STRING
        A = TRUE; // BOOLEAN

    2)(STRING DATATYPE) SINGLE AND DOUBLE QUOTATION IN STRING
    EX: LET A = 'NEHAL'; // SINGLE
        LET B = "NEHAL"; // DOUBLE

        IF WE WANT SINGLE QUOTE IN STRING USING SINGLE QUOTATION THEN IT WILL NOT WORK; (IF WE WANT NEH'HAL IN SINGLE QUOTE)
        EX: LET A = 'NEH'HAL'; // IT WILL NOT WORK;

        ==> IN THIS CASE, WE WILL HAVE TO USE DOUBLE QUOTE TO REPRESENT SINGLE QOUTE IN STRING
        EX: LET A = "NEH'AL"; // IT WILL WORK;

        ==> WHEN WE WANT DOUBLE QUOTE IN OUR STWING (NEH"AL);
            THEN WE WILL USE SINGLE QUOTATION TO REPRESENT DOUBLE QUOTE
            EX: LET A = 'NEH"AL'; // IT WILL WORK

        IF WE WANT DOUBLE QUOTATION IN OUR STRING USING DOUBLE QUOTATION;
        ("NEH"AL") THEN USE 'BACK SLASH' BEFORE MIDDLE DOUBLE QUOTE;
        EX: LET A = "NEH\"AL"; // IT WILL WORK AS NEH"AL
        
        (IT WILL SAME WITH SINGLE QUOTE ALSO)
        EX: LET A = 'NEH\'AL'; // IT WILL WORK AS NEH'AL

*/
 

/*
    ------- NUMBER DATA TYPE ------

    1) LET A = 10; // NUMBER
    2) LET A = 10.23; // NUMBER
    3) LET A = -10.2233; // NUMBER

    ALL ARE NUMBERS. NO DIFFERENCE
    (WHEN NUMBER ADD WITH STEING, IT WILL BECOME STRING
        EX: LET A = 5; //NUMBER
            LET B = '5' // STRING
            RESULT => 55 // STRING )
*/

